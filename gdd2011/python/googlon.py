def is_verb(word):
    return len(word) == 7 and word[-1] not in ABACATE

def is_first_person_verb(word):
    return is_verb(word) and word[0] not in ABACATE

def to_integer(word):
    integer_value = 0

    for index in range(len(word)):
        integer_value += (ALPHABET.index(word[index]) * (NUMBER_MULTIPLER ** index))

    return integer_value

def is_magic_number(word):
    return len(word) < 7 and len(word) % 2 == 0 and to_integer(word) % 13 == 0

